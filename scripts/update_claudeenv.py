def load_recent_official(days: int = 14) -> list[dict]:
    items = []
    if not RAW_NEWS.exists():
        return items
    cutoff = dt.date.today() - dt.timedelta(days=days)
    for jsonl in sorted(RAW_NEWS.glob("*.jsonl"), reverse=True):
        try:
            file_date = dt.date.fromisoformat(jsonl.stem)
            if file_date < cutoff:
                continue
        except ValueError:
            continue
        for line in jsonl.read_text(encoding="utf-8").splitlines():
            try:
                row = json.loads(line)
            except Exception:
                continue
            if "official" in row.get("tags", []):
                items.append(row)
    items.sort(key=lambda r: r.get("fetched_at", ""), reverse=True)
    return items[:50]


