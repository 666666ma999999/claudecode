def scan_hooks() -> dict:
    hooks_dir = CLAUDE_DIR / "hooks"
    active_from_settings = {}
    settings = CLAUDE_DIR / "settings.json"
    if settings.exists():
        try:
            d = json.loads(settings.read_text())
            for event, items in d.get("hooks", {}).items():
                for it in items:
                    matcher = it.get("matcher", "*")
                    for h in it.get("hooks", []):
                        cmd = h.get("command", "")
                        # extract referenced script path
                        m = re.search(r"~/\.claude/hooks/(\S+)", cmd)
                        script = m.group(1) if m else cmd[:60]
                        active_from_settings.setdefault(script, []).append(f"{event}[{matcher}]")
        except Exception as e:
            print(f"[warn] settings.json parse: {e}", file=sys.stderr)

    active, dormant = [], []
    if hooks_dir.exists():
        for f in sorted(hooks_dir.glob("*")):
            if not f.is_file():
                continue
            if f.name.startswith(("_", ".")) or f.name in ("CLAUDE.md", "README.md"):
                continue
            registrations = active_from_settings.get(f.name, [])
            entry = {
                "name": f.name,
                "events": ", ".join(registrations) if registrations else "—",
                "last_modified": dt.datetime.fromtimestamp(f.stat().st_mtime).strftime("%Y-%m-%d"),
            }
            (active if registrations else dormant).append(entry)
    return {"active": active, "dormant": dormant}


