def scan_collector_health() -> list[dict]:
    """各 collector の最終更新を見て status を判定。"""
    today = dt.date.today()
    out = []
    raw_root = VAULT / ".raw"
    for c in COLLECTORS:
        # path_glob は .raw/ 相対
        rel_glob = c["path_glob"]
        matches = list(raw_root.glob(rel_glob.replace(".raw/", "")))
        if matches:
            last_mtime = max(m.stat().st_mtime for m in matches if m.is_file()) if any(m.is_file() for m in matches) else 0
            last_date = dt.date.fromtimestamp(last_mtime) if last_mtime else None
            last_str = last_date.isoformat() if last_date else "—"
            days = (today - last_date).days if last_date else 9999
        else:
            last_str = "(なし)"
            days = 9999

        # status (auto: 健全性表示 / manual+passive: 最終使用日のみ意味あり)
        if c["mode"] == "auto":
            if days <= c["stale_threshold_days"]:
                status = "🟢 ok"
            elif days <= c["stale_threshold_days"] * 2:
                status = "🟡 stale"
            else:
                status = "🔴 broken?"
        elif c["mode"] == "manual":
            # manual は「健全/故障」概念なし。最後の使用からの経過のみ表示
            status = "on-demand"
        else:  # passive
            status = "—"

        out.append({
            **c,
            "last_updated": last_str,
            "stale_days": days if days < 9999 else "—",
            "status": status,
        })
    return out


