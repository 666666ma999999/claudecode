def build_drift(official: list[dict], skills: list[dict]) -> list[dict]:
    """Detect potential drift: official P0/P1 items that may relate to existing assets."""
    drift = []
    skill_names = {s["name"].lower() for s in skills}
    for r in official:
        if r.get("priority") not in ("P0", "P1"):
            continue
        title_lower = (r.get("title", "") + " " + r.get("summary", "")).lower()
        matched = [n for n in skill_names if n in title_lower and len(n) > 4]
        drift.append({
            "priority": r.get("priority", "P2"),
            "title": r.get("title", "")[:120],
            "url": r.get("url", ""),
            "source": r.get("source", ""),
            "date": (r.get("published_at") or r.get("fetched_at") or "")[:10],
            "matched_skills": matched,
            "article_candidate": "yes" if r.get("priority") == "P0" else "no",
            "posted_from": "",
        })
    return drift


def render_drift(drift: list[dict], health: dict) -> str:
    """Drift report with priority sections + source health flags."""
    p0 = [d for d in drift if d["priority"] == "P0"]
    p1 = [d for d in drift if d["priority"] == "P1"]
    now = dt.datetime.now()

    # source health alerts
    alerts = []
    for src, info in (health or {}).items():
        last = info.get("last_success_at")
        if not last:
            continue
        try:
            last_dt = dt.datetime.fromisoformat(last)
            hours = (now - last_dt).total_seconds() / 3600
            if hours > 72:
                alerts.append(f"- 🔴 **{src}**: last_success_at = {last} ({hours:.0f}h ago)")
        except Exception:
            pass

    n_alerts = len(alerts)
    summary_status = f"現況: P0 **{len(p0)}** 件、P1 **{len(p1)}** 件" + (f"、🔴 source 沈黙 **{n_alerts}** 件" if n_alerts else "、source health 全 OK")
    lines = [
        "---",
        "auto-generated: true",
        f"updated: {now.isoformat(timespec='seconds')}",
        "tags: [claude-env, drift]",
        "source-script: ~/.claude/scripts/update_claudeenv.py",
        "update-trigger: daily + on-demand (depends on official-updates.md)",
        "scope: replacement candidates (official P0/P1 vs custom assets)",
        "related: [[official-updates]], [[skills-catalog]], [[hooks-catalog]], [[_README]]",
        "---",
        "",
        f"# Drift Watch (P0={len(p0)}, P1={len(p1)})",
        "",
        "> 公式更新 vs 自作環境のリプレイス候補。人手レビュー必要。",
        "",
        "## Summary",
        "",
        summary_status + "。  ",
        "このファイルは **公式の新機能/Breaking 変更が、自作 skill/hook と被るか** を P0/P1 別に追跡する監視表です。  ",
        "各項目: タイトル+URL / source / matched_skills (自動推測) / **article_candidate** (X 投稿候補フラグ) / **posted_from** (投稿済み記事 ID)。  ",
        "更新は daily 8:00 + on-demand。source 沈黙 (72h 無更新) 時は冒頭に 🔴 アラート表示。  ",
        "まず P0 を確認 → 自環境への影響評価 → リプレイスする or 自作維持を判断。`article_candidate: yes` は `~/.claude/scripts/article_bridge.py` で `~/Desktop/biz/make_article/data/claudeenv_queue/` に転記され、make_article で記事化 → X 投稿。  ",
        "関連: [[official-updates]] (一次情報) / [[skills-catalog]] (置換対象確認) / [[_README]]",
        "",
    ]
    if alerts:
        lines += ["## 🔴 Source Health Alerts", "", *alerts, ""]

    for label, group in (("🚨 P0 (Breaking / Critical)", p0), ("✨ P1 (New Features)", p1)):
        lines += [f"## {label}", ""]
        if not group:
            lines += ["_(該当なし)_", ""]
            continue
        for d in group:
            link = f"[{d['title']}]({d['url']})" if d["url"] else d["title"]
            matched = ", ".join(d["matched_skills"]) if d["matched_skills"] else "—"
            lines += [
                f"### {link}",
                f"- **source**: {d['source']} / {d['date']}",
                f"- **matched_skills**: {matched}",
                f"- **article_candidate**: {d['article_candidate']}",
                f"- **posted_from**: {d['posted_from'] or '(未投稿)'}",
                "",
            ]
    return "\n".join(lines) + "\n"


