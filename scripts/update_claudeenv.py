def scan_mcp_and_plugins() -> dict:
    mcp_servers = []
    plugins = []
    mcp_path = CLAUDE_DIR / ".mcp.json"
    if mcp_path.exists():
        try:
            d = json.loads(mcp_path.read_text())
            for name, conf in d.get("mcpServers", {}).items():
                mcp_servers.append({
                    "name": name,
                    "type": conf.get("type", "stdio"),
                    "command": (conf.get("command", "") + " " + " ".join(conf.get("args", [])))[:80],
                    "url": conf.get("url", ""),
                })
        except Exception as e:
            print(f"[warn] .mcp.json parse: {e}", file=sys.stderr)

    plugins_path = CLAUDE_DIR / "plugins/installed_plugins.json"
    if plugins_path.exists():
        try:
            d = json.loads(plugins_path.read_text())
            # installed_plugins.json structure varies; handle a few common shapes
            if isinstance(d, dict):
                for marketplace, plist in d.items():
                    if isinstance(plist, dict):
                        for pname, pmeta in plist.items():
                            plugins.append({
                                "name": pname,
                                "marketplace": marketplace,
                                "enabled": pmeta.get("enabled", True) if isinstance(pmeta, dict) else True,
                            })
                    elif isinstance(plist, list):
                        for p in plist:
                            plugins.append({"name": str(p), "marketplace": marketplace, "enabled": True})
        except Exception as e:
            print(f"[warn] installed_plugins.json parse: {e}", file=sys.stderr)
    return {"mcp_servers": mcp_servers, "plugins": plugins}


