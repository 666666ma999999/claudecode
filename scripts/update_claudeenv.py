def scan_skills() -> list[dict]:
    skills_dir = CLAUDE_DIR / "skills"
    out = []
    if not skills_dir.exists():
        return out
    for skill_md in sorted(skills_dir.glob("*/SKILL.md")):
        name = skill_md.parent.name
        if name.startswith("_"):
            continue
        meta = parse_frontmatter(skill_md)
        out.append({
            "name": name,
            "description": (meta.get("description") or "").splitlines()[0][:160],
            "allowed_tools": meta.get("allowed-tools", ""),
            "disable_model_invocation": meta.get("disable-model-invocation", ""),
            "last_modified": dt.datetime.fromtimestamp(skill_md.stat().st_mtime).strftime("%Y-%m-%d"),
            "path": str(skill_md.relative_to(HOME)),
        })
    return out


def parse_frontmatter(md_path: Path) -> dict:
    try:
        text = md_path.read_text(encoding="utf-8", errors="ignore")
    except Exception:
        return {}
    if not text.startswith("---"):
        return {}
    end = text.find("\n---", 4)
    if end == -1:
        return {}
    body = text[4:end]
    meta = {}
    cur_key = None
    for line in body.splitlines():
        m = re.match(r"^(\w[\w-]*?):\s*(.*)$", line)
        if m:
            cur_key = m.group(1)
            meta[cur_key] = m.group(2).strip()
        elif cur_key and line.startswith("  "):
            meta[cur_key] += " " + line.strip()
    return meta


