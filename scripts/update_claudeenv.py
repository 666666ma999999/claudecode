def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument(
        "--target",
        choices=["skills", "hooks", "rules", "agents", "commands", "mcp", "official", "drift", "health", "readme", "all"],
        default="all",
    )
    args = ap.parse_args()

    all_targets = ["skills", "hooks", "rules", "agents", "commands", "mcp", "official", "drift", "health", "readme"]
    targets = all_targets if args.target == "all" else [args.target]
    skills_data = None
    official_data = None
    health = {}
    if HEALTH.exists():
        try:
            health = json.loads(HEALTH.read_text())
        except Exception:
            pass

    changed = []
    if "skills" in targets or "drift" in targets:
        skills_data = scan_skills()
    if "official" in targets or "drift" in targets:
        official_data = load_recent_official()

    if "skills" in targets:
        if write_if_changed(OUT_DIR / "skills-catalog.md", render_skills(skills_data)):
            changed.append("skills-catalog.md")
    if "hooks" in targets:
        if write_if_changed(OUT_DIR / "hooks-catalog.md", render_hooks(scan_hooks())):
            changed.append("hooks-catalog.md")
    if "rules" in targets:
        if write_if_changed(OUT_DIR / "rules-catalog.md", render_rules(scan_rules())):
            changed.append("rules-catalog.md")
    if "agents" in targets:
        if write_if_changed(OUT_DIR / "agents-catalog.md", render_agents(scan_agents())):
            changed.append("agents-catalog.md")
    if "commands" in targets:
        if write_if_changed(OUT_DIR / "commands-catalog.md", render_commands(scan_commands())):
            changed.append("commands-catalog.md")
    if "mcp" in targets:
        if write_if_changed(OUT_DIR / "mcp-and-plugins.md", render_mcp_plugins(scan_mcp_and_plugins())):
            changed.append("mcp-and-plugins.md")
    if "official" in targets:
        if write_if_changed(OUT_DIR / "official-updates.md", render_official(official_data)):
            changed.append("official-updates.md")
    if "drift" in targets:
        drift = build_drift(official_data or [], skills_data or scan_skills())
        if write_if_changed(OUT_DIR / "drift-watch.md", render_drift(drift, health)):
            changed.append("drift-watch.md")
    if "health" in targets:
        if write_if_changed(OUT_DIR / "collector-health.md", render_collector_health(scan_collector_health())):
            changed.append("collector-health.md")
        changed += stamp_freshness()  # ✅1c 鮮度スタンパ (daily 8:00 に相乗り・機械メタ行のみ)
    if "readme" in targets:
        if write_if_changed(OUT_DIR / "ClaudeEnv_ope.md", README_CONTENT):
            changed.append("ClaudeEnv_ope.md")

    print(f"[update_claudeenv] target={args.target}, changed={changed or 'none'}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
